public class relationaloprtr {

    public static void main(String[] args) {

        float a = 15.0f;
        float b = 20.75f;
        float c = 15.0f;

        System.out.println("A  = " + a);
        System.out.println("B  = " + b);
        System.out.println("C  = " + c);
        System.out.println("________________");
        System.out.println("A < B = " + (a < b));
        System.out.println("A > B = " + (a > b));
        System.out.println("A == B = " + (a == b));
        System.out.println("A <= B = " + (a <= b));
        System.out.println("A >= B = " + (a >= b));
        System.out.println("A != B = " + (a != b));
    }
}
