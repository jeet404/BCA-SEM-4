import java.lang.*;

public class methClassDemo {
    public static void main(String[] args) {
        int v1 = -1;
        float v2 = .5f;
        System.out.println("Initial Value of V1 = " + v1);
        System.out.println("Initial Value of V2 = " + v2);
        int absI = Math.abs(v1);
        float absF = Math.abs(v2);
        System.out.println("Absolute Value of V1 = " + absI);
        System.out.println("Absolute Value of V2 = " + absF);
    }
}
