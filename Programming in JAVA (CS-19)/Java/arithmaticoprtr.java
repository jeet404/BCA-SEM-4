public class arithmaticoprtr {

    public static void main(String[] args) {
        float a = 20.5f;
        float b = 6.4f;
        System.out.println("A = " + a);
        System.out.println("B = " + b);
        System.out.println("________________");
        System.out.println("A + B = " + (a + b));
        System.out.println("A - B = " + (a - b));
        System.out.println("A * B = " + (a * b));
        System.out.println("A / B = " + (a / b));
        System.out.println("A % B = " + (a % b));
    }
}
